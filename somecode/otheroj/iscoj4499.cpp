#pragma GCC optimize("Ofast")
#include <bits/stdc++.h>
using namespace std;

#define endl '\n'

int n, m, q;

signed main() {
    ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);
    scanf("%d%d%d",&n,&m,&q);
    while(m--) scanf("%d%d",&n,&n);
    while(q--) {
        scanf("%d%d",&n,&n);
        cout<<"YES"<<endl;
    }
    return 0;
}
